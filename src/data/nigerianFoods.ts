/**
 * Get a food by its ID
 */
export function getFoodById(id: string): Food | undefined {
  return nigerianFoods.find((food) => food.id === id);
}

/**
 * Get all foods in a specific category
 */
export function getFoodsByCategory(category: string): Food[] {
  return nigerianFoods.filter((food) => food.category === category);
}

/**
 * Get the total count of foods in the database
 */
export function getFoodCount(): number {
  return nigerianFoods.length;
}
